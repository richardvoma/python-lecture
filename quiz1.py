# Use the `+=` assignment operator to add 5 to the `count` variable.
count = 10

count += 5  # count=count+5
print(count)  # Print the value of `count` to the console.

# use `-=` assignment operator to subtract 3 from the `count` variable.
count -= 3  # count=count-3
print(count)  # Print the value of `count` to the console.

# use the `*=` assignment operator to multiply the `count` variable by 2.
# count=count*2
count *= 2
print(count)  # Print the value of `count` to the console.
